







CREATE PROCEDURE NX_SP_ACIK_HAREKET
	(
        @KULLANICI_KODU varchar(20),
        @HAREKET_TIPI char(2),
        @HAREKET_ID int Output
	)
AS
	SELECT @HAREKET_ID=HAREKET_ID FROM NX_HAREKETLER
	WHERE KULLANICI_KODU=@KULLANICI_KODU AND
		 HAREKET_TIPI=@HAREKET_TIPI AND
		 DURUM='Y' /*YENİ*/
	
	RETURN @@ROWCOUNT


