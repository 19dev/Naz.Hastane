CREATE VIEW dbo.SGK18
AS
SELECT     TOP 100 PERCENT H.TANIM, H.GRUP, H.CODE, H.KDV, H.TARIH
FROM         dbo.HIZIL H INNER JOIN
                      dbo.BEHAND B ON H.KNR = B.KNR
WHERE     (H.KDV = '18') AND (B.MEDFATURATARIHI BETWEEN CONVERT(DATETIME, '2008-10-01 00:00:00') AND CONVERT(DATETIME, '2008-10-31 23:59:59'))
ORDER BY H.TARIH DESC
