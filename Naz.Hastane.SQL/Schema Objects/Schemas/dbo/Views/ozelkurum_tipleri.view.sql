
CREATE VIEW dbo.ozelkurum_tipleri
AS
SELECT CASE WHEN SLB='0001' THEN '0001' WHEN SLB='0006' THEN 'SGK' WHEN SLB='0008' THEN '0001' ELSE SLB END AS KURUMADI FROM   dbo.KEYDAT
WHERE     (SLK = '36')
GROUP BY CASE WHEN SLB='0001' THEN '0001' WHEN SLB='0006' THEN 'SGK' WHEN SLB='0008' THEN '0001' ELSE SLB END

